package Variables;

public class Caracteres_Especiales {
    public static void main(String[] args) {
        String Nombre = "Simon";
        System.out.println("Linea Nueva: \n" + Nombre); // \n es para saltar linea y se imprime la variable al final
        System.out.println("Tabulador: \t" + Nombre); // Es para tabuluar como el tab
        System.out.println("Retroceder: \b" + Nombre);// es para retroceder dos espacios
        System.out.println("Comilla Simple: \'" + Nombre + "\'");
        System.out.println("Comilla Doble: \"" + Nombre + "\"");
    }
}
